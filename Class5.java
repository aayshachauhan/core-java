//LOGICAL OPERATOR
public class Class5 {

	public static void main(String[] args) {

		int subject1 = 20;
		int subject2 = 60;
		
		if((subject1 >= 35) && (subject2 >=35))
		{
			System.out.println("Condition is true");
		} 
		else 
		{
			System.out.println("Condition is false");
		}
		
		int marks1 = 20;
		int marks2 = 50;
		if((marks1 >= 35) || (marks2 >=35))
		{
			System.out.println("Condition is true");
		} 
		else 
		{
			System.out.println("Condition is false");
		}
			
	}

}
