//AIRTHMETIC OPERATOR AND ASSIGNMENT OPERATOR

public class Class3 {

	public static void main(String[] args) {
		
		int x =10;
		x = x+1; 

//x++; post increment method
// here we increment number by these two method x+1 and x++.
// with these method we get increment

		System.out.println(x);
		
		int a=5;
		System.out.println(a++);

//	first it print the given value of x then it will add one with x value by ++ post increment
		
		int y =20;
 		System.out.println(y++);
		System.out.println(y);
		
		int z =30;
 		System.out.println(++z);
		System.out.println(z);

//x++ it print the value first then increment by one number
//++x first it increment by one number then give added value
		
		int i = 50;
		i += 5; 

		System.out.println(i);

// = is assignment operator
/*
  += operator is same as this i = i + 5
  goes same with multiply subtract division 
  *= /= -=
 */
		
}

}
